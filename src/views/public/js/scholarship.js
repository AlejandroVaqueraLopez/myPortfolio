import {scholarshipListData} from "./data.js";

const scholarshipList = document.querySelector("#scholarshipList");

//render all the items iterating data objects 
const render = (scholarshipListData) => {
	scholarshipListData.forEach((obj) => {
		itemTemplate(obj);
	});
}

//template techsItem generator
const itemTemplate = (obj) => {
	const {name,image} = obj;

	const li = document.createElement("li");
	li.classList.add("scholarshipItem");
	li.id = `${name}`;

	const scholarshipImageContainer = document.createElement("div");
	scholarshipImageContainer.classList.add("scholarshipImageContainer","schoolImageHoverAnim");

	const img = document.createElement("img");
	img.src = `${image}`;

	scholarshipImageContainer.appendChild(img);

	li.appendChild(scholarshipImageContainer);

	scholarshipList.appendChild(li);

	//console.log(a);
		/*
				<!--all items are generated by templates (proyects.js)-->
					<li id="${name}">
						<img src="" alt="">
					</li>
	;*/
}

render(scholarshipListData);

